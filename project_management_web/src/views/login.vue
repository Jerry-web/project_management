<template>
  <el-form :model="loginForm" :rules="rules" ref="loginForm" label-position="left" label-width="0px" class="demo-ruleForm login-container">
    <h3 class="title">用户登录</h3>
    <el-form-item prop="account">
      <el-input type="text" v-model="loginForm.user_name" auto-complete="off" placeholder="账号"></el-input>
    </el-form-item>
    <el-form-item prop="checkPass">
      <el-input type="password" v-model="loginForm.user_password" auto-complete="off" placeholder="密码"></el-input>
    </el-form-item>
    <el-form-item style="width:100%;">
      <el-button type="primary" native-type='submit' style="width:100%;" @click.native.prevent="loginSubmit" :loading="logining">登录</el-button>
    </el-form-item>
    <el-checkbox v-model="checked" checked class="remember sky">记住密码</el-checkbox>
  </el-form>
</template>
<script>
  export default {
    data() {
      return {
        logining: false,
        loginForm: {
          user_name: null,
          user_password: null
        },
        rules: {
          user_name: [
            { required: true, message: '请输入账号', trigger: 'blur' },
          ],
          user_password: [
            { required: true, message: '请输入密码', trigger: 'blur' },
          ]
        },
        checked: true
      };
    },
    methods: {
      loginSubmit(ev) {
        var _this = this;
        _this.$refs['loginForm'].validate((valid) => {
          if (valid) {
            _this.logining = true;
            _this.$http({
              method: 'POST',
              url: _this.config.baseUrl + 'user/login',
              data: _this.loginForm
            }).then(function (data) {
              var result = data.data;
              var response = result.code;
              _this.logining = false;
              if (response == 0) {
                sessionStorage.setItem('user',JSON.stringify(result.user))
                _this.$router.push('/projectlist');

              } else {
                _this.$message.error('用户名或密码错误！！');
              }
            })
          } else {
            console.log('error submit!!');
            return false;
          }
        });
      }
    },
    mounted(){
      var loginUser=localStorage.getItem('user');
      if(loginUser&&loginUser.length>0){
        loginUser=JSON.parse(loginUser);
        this. loginForm= {
          user_name: loginUser.user_name,
          user_password: loginUser.user_password
        }
      }
    }
  }
</script>

